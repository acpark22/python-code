message = input("Tell me something, and I will repeat it back to you: ")
print(message)

# the input() function takes one argument: the prompt, or instructions, that we want to display to the user so they know what to do. Python runs the first line, the user sees the prompt 'Tell me something, and I will repeat it back to you: . The program waits while the user enters their response and continues after the user presses ENTER. The response is assigned to the variable, 'message', then print(message) displays the input back to the user.

