current_number = 1
while current_number <= 5:
    print(current_number)
    current_number += 1

# we start counting from 1 by assigning current_number the value 1. The 'while' loop is then set to keep running as long as the value of current_number is less than or equal to 5. The code inside the loop prints the value of current_number and then adds 1 to that value with current_number += 1. 
