magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(magician)

# the 'for magician' loop tells Python to pull a name from the list 'magicians' and associate it with the variable magician. Python then repeats lines 2 and 3 once for each name in the list.  it might help to read, "For every magician in the list of magicians, print the magician's name."


